def print_tic_tac_toe(horiz_char, vert_char):
    print('x {0} x {1} x'.format(vert_char, vert_char))
    print('{0} {1} {2} {3} {4}'.format(horiz_char, horiz_char, horiz_char, horiz_char, horiz_char))
    print('x {0} x {1} x'.format(vert_char, vert_char))
    print('{0} {1} {2} {3} {4}'.format(horiz_char, horiz_char, horiz_char, horiz_char, horiz_char))
    print('x {0} x {1} x'.format(vert_char, vert_char))

print_tic_tac_toe('~', '!')